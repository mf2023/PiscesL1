# Current language, default to Chinese
_current_lang = "zh"

# Set the current language
# Args:
#     lang (str): The language code to set. If the language code is not in _MESSAGES, defaults to "zh".
# Returns:
#     None
def set_lang(lang: str) -> None:
    global _current_lang
    _current_lang = lang if lang in _MESSAGES else "zh"

# Translate a message by key
# Args:
#     key (str): The translation key.
#     lang (str | None, optional): The target language code. If None, use the current language.
# Returns:
#     str: The translated message. If the translation is not found, try English. If still not found, return the key itself.
def t(key: str, *, lang: str | None = None) -> str:
    # Use the specified language or the current language
    L = (lang or _current_lang)
    # Get the message dictionary for the specified language, default to Chinese if not found
    m = _MESSAGES.get(L) or _MESSAGES["zh"]
    # Try to get the translation, fallback to English if not found, then return the key itself
    return m.get(key) or _MESSAGES["en"].get(key) or key

