    def _verify(self, inputs: Dict[str, Any], content_type: str) -> PiscesLxOperatorResult:
        content = inputs.get("content")
        expected = inputs.get("expected", {})
        
        extract_result = self._extract(inputs, content_type)
        
        if not extract_result.output.get("watermark_found", False):
            return PiscesLxOperatorResult(
                operator_name=self.name,
                status=PiscesLxOperatorStatus.SUCCESS,
                output={"verified": False, "reason": "No watermark found"},
                metadata={"content_type": content_type}
            )
        
        payload = extract_result.output.get("payload", {})
        verified = True
        failed_checks = []
        
        if expected.get("model_id") and payload.get("model") != expected["model_id"]:
            verified = False
            failed_checks.append("model_id")
        
        if expected.get("standard") and payload.get("compliance", {}).get("standard") != expected["standard"]:
            verified = False
            failed_checks.append("standard")
        
        return PiscesLxOperatorResult(
            operator_name=self.name,
            status=PiscesLxOperatorStatus.SUCCESS,
            output={
                "verified": verified,
                "payload": payload,
                "failed_checks": failed_checks
            },
            metadata={"content_type": content_type}
        )
    
    def _detect_content_type(self, content: Any) -> str:
        if isinstance(content, str):
            return "text"
        elif isinstance(content, torch.Tensor):
            shape = content.shape
            if len(shape) == 3 and shape[0] in [1, 3, 4]:
                return "image"
            elif len(shape) == 1 or (len(shape) == 2 and shape[0] == 1):
                return "audio"
            elif len(shape) == 4:
                return "video"
        return "unknown"
    
    def _create_payload(self, payload: Dict[str, Any], metadata: Dict[str, Any], 
                       content_type: str) -> PiscesLxWatermarkPayload:
        watermark_payload = PiscesLxWatermarkPayload(
            model_id=self.config.model_id,
            version=self.config.version,
            user_hash=hashlib.sha256(
                metadata.get("user_id", "anonymous").encode()
            ).hexdigest()[:16],
            compliance={
                "standard": self.config.standard.value,
                "jurisdiction": self.config.jurisdiction.code,
                "risk_level": self.config.risk_level.value,
                "mandatory_disclosure": self.config.mandatory_disclosure,
                "tamper_evidence": self.config.tamper_evidence,
                "audit_required": self.config.audit_enabled,
                "content_type": content_type
            }
        )
        watermark_payload.trace_chain = str(uuid.uuid4())
        return watermark_payload
    
    def _embed_bits_in_text(self, content: str, bitstream: str, 
                          trace_chain: str) -> str:
        def bits_to_zwc(bitstream_inner: str) -> List[str]:
            if len(bitstream_inner) % 2 == 1:
                bitstream_inner += '0'
            symbols = [bitstream_inner[i:i+2] for i in range(0, len(bitstream_inner), 2)]
            return [self.zero_width_chars[int(s, 2)] for s in symbols]
        
        zwc_stream = bits_to_zwc(bitstream)
        
        key = f"{trace_chain}_{self.config.jurisdiction.code}"
        redundancy = self.config.redundancy_level
        
        watermarked = content
        
        for layer in range(redundancy):
            positions = []
            for i, _ch in enumerate(content):
                hash_input = f"{key}_{layer}_{i}"
                hash_val = hashlib.md5(hash_input.encode()).hexdigest()
                if hash_val[:2] == '00':
                    positions.append(i)
            
            temp = list(watermarked)
            idx = 0
            for pos in sorted(positions, reverse=True):
                if idx < len(zwc_stream):
                    temp.insert(pos, zwc_stream[idx])
                    idx += 1
            watermarked = ''.join(temp)
            
            if idx >= len(zwc_stream):
                break
        
        return watermarked
    
    def _embed_bits_in_audio(self, audio: torch.Tensor, bits: str, 
                            trace_chain: str) -> torch.Tensor:
        sample_rate = 44100
        min_freq = 18000
        max_freq = 20000
        amplitude = 0.01
        spread_factor = 10
        
        x = audio.flatten()
        n_fft = 1024
        hop = n_fft // 4
        win = torch.hann_window(n_fft, device=x.device, dtype=x.dtype)
        X = torch.stft(x, n_fft=n_fft, hop_length=hop, window=win, return_complex=True)
        
        freqs = torch.fft.rfftfreq(n_fft, d=1.0 / sample_rate).to(x.device)
        band_mask = (freqs >= min_freq) & (freqs <= max_freq)
        idx_band = torch.where(band_mask)[0]
        
        if idx_band.numel() == 0:
            return audio
        
        seed = int(hashlib.sha256(trace_chain.encode()).hexdigest(), 16) & 0x7FFFFFFF
        torch.manual_seed(seed)
        pn = torch.sign(torch.rand(spread_factor, device=x.device) - 0.5)
        
        bit_idx = 0
        for t in range(X.shape[1]):
            if bit_idx >= len(bits):
                break
            b = 1 if bits[bit_idx] == '1' else -1
            chips = pn[:min(spread_factor, idx_band.numel())] * b
            X[idx_band[:chips.numel()], t] = X[idx_band[:chips.numel()], t] * (1.0 + amplitude * chips)
            if t % 2 == 1:
                bit_idx += 1
        
        y = torch.istft(X, n_fft=n_fft, hop_length=hop, window=win, length=x.numel())
        return y.view(audio.shape)
    
    def _extract_bits_from_audio(self, audio: torch.Tensor) -> str:
        sample_rate = 44100
        min_freq = 18000
        max_freq = 20000
        spread_factor = 10
        
        x = audio.flatten()
        n_fft = 1024
        hop = n_fft // 4
        win = torch.hann_window(n_fft, device=x.device, dtype=x.dtype)
        X = torch.stft(x, n_fft=n_fft, hop_length=hop, window=win, return_complex=True)
        
        freqs = torch.fft.rfftfreq(n_fft, d=1.0 / sample_rate).to(x.device)
        band_mask = (freqs >= min_freq) & (freqs <= max_freq)
        idx_band = torch.where(band_mask)[0]
        
        if idx_band.numel() == 0:
            return ""
        
        pn = torch.sign(torch.rand(spread_factor, device=x.device) - 0.5)
        
        votes = []
        for t in range(0, X.shape[1] - 1, 2):
            mag = torch.abs(X[idx_band, t])
            chips = pn[:min(spread_factor, idx_band.numel())]
            corr = (mag[:chips.numel()] * chips).sum()
            votes.append('1' if corr > 0 else '0')
        
        bitstream = ''.join(votes)
        
        def majority_vote_decode(bits: str) -> str:
            if len(bits) < 3:
                return bits
            result = []
            for i in range(0, len(bits) - 2, 3):
                tri = bits[i:i+3]
                if tri.count('1') >= 2:
                    result.append('1')
                elif tri.count('0') >= 2:
                    result.append('0')
            return ''.join(result)
        
        return majority_vote_decode(bitstream)
    
    def embed_text(self, content: str, payload: Dict[str, Any], 
                  metadata: Optional[Dict[str, Any]] = None) -> str:
        """Convenience method to embed watermark in text."""
