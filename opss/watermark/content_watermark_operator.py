    def _extract_from_text(self, text: str) -> PiscesLxOperatorResult:
        if not isinstance(text, str):
            return PiscesLxOperatorResult(
                operator_name=self.name,
                status=PiscesLxOperatorStatus.FAILED,
                error="Text content must be a string"
            )
        
        try:
            rev_map = {v: k for k, v in {
                "00": self.zero_width_chars[0],
                "01": self.zero_width_chars[1],
                "10": self.zero_width_chars[2],
                "11": self.zero_width_chars[3]
            }.items()}
            
            symbols = []
            for ch in text:
                sym = rev_map.get(ch)
                if sym is not None:
                    symbols.append(sym)
            
            if not symbols:
                return PiscesLxOperatorResult(
                    operator_name=self.name,
                    status=PiscesLxOperatorStatus.SUCCESS,
                    output={"watermark_found": False, "reason": "No watermark symbols found"},
                    metadata={"content_type": "text"}
                )
            
            bitstream = ''.join(symbols)
            
            result = self.protocol_operator._decode({
                "bitstream": bitstream,
                "redundancy": self.config.redundancy_level
            })
            
            if result.is_success():
                return PiscesLxOperatorResult(
                    operator_name=self.name,
                    status=PiscesLxOperatorStatus.SUCCESS,
                    output={
                        "watermark_found": True,
                        "payload": result.output["payload"]
                    },
                    metadata={"content_type": "text"}
                )
            else:
                return PiscesLxOperatorResult(
                    operator_name=self.name,
                    status=PiscesLxOperatorStatus.SUCCESS,
                    output={"watermark_found": False, "reason": "Decode failed"},
                    metadata={"content_type": "text"}
                )
                
        except Exception as e:
            return PiscesLxOperatorResult(
                operator_name=self.name,
                status=PiscesLxOperatorStatus.FAILED,
                error=str(e)
            )
    
