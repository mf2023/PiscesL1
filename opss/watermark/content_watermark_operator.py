"""
Content Watermark Operator

This module implements multi-modal content watermarking for AI-generated content.
It supports text, image, and audio watermarking with comprehensive embedding
and extraction capabilities.

Watermarking Modalities:

1. Text Watermarking:
   - Zero-width characters: U+200B (ZWJ), U+200C (ZWNJ), U+200D (LRM), U+FEFF (BOM)
   - 2-bit encoding per character: 00, 01, 10, 11
   - Redundancy encoding (1-5x) for robustness
   - Pseudo-random distribution based on seed
   - Invisible to humans, machine-readable

2. Image Watermarking:
   - DCT frequency domain embedding
   - Mid-band coefficient modification
   - Pairwise magnitude relationship encoding
   - Visible overlay option for explicit labeling
   - JPEG compression resistance

3. Audio Watermarking:
   - STFT frequency domain embedding
   - Ultrasonic frequency band (18-20kHz)
   - DSSS (Direct Sequence Spread Spectrum)
   - Frame synchronization for reliability
   - Robust against re-encoding

Key Features:
    - Automatic content type detection
    - Unified interface for all modalities
    - Configurable strength and redundancy
    - Seed-based reproducibility
    - Protocol integration for payload framing
    - Comprehensive metadata embedding

Usage Examples:
    >>> from opss.watermark.content_watermark_operator import PiscesLxContentWatermarkOperator
    >>> operator = PiscesLxContentWatermarkOperator()
    >>> 
    >>> # Embed watermark in text
    >>> watermarked_text = operator.embed_text(
    ...     "Hello, World!",
    ...     payload={"model": "PiscesL1", "user_id": "user123"}
    ... )
    >>> 
    >>> # Embed watermark in image
    >>> watermarked_image = operator.embed_image(
    ...     image_tensor,
    ...     payload={"model": "PiscesL1"}
    ... )
    >>> 
    >>> # Extract watermark from content
    >>> extracted = operator.extract(content)

Author: PiscesL1 Development Team
Version: 1.0.0
"""

